# Inches to cm 

def cm_to_inches(Centimeter):
    inches = Centimeter/2.54
    return inches

CM = 165
inch = cm_to_inches(CM)

print(f"{CM} Centimeter ={inch:.2f} Inches")


# Cm to Feet

def cm_to_inches(Centimeter):
    feet = Centimeter/30.48
    return feet

CM = 170
Feet = cm_to_inches(CM)

print(f"{CM} Centimeter ={Feet:.2f} Feets")